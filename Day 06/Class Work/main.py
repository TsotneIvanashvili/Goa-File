# 1) კომენტარის სახით ახსენი, რა არის snake_case, camelCase და PascalCase.
# snake_case — სიტყვები იწერება პატარა ასოებით და ერთმანეთისგან გამოიყოფა ქვედატირით (_)
# მაგალითად: my_name, user_age, total_sum

# camelCase — პირველი სიტყვა იწყება პატარა ასოთი,
# ხოლო ყოველი შემდეგი სიტყვა იწყება დიდი ასოთი
# მაგალითად: myName, userAge, totalSum

# PascalCase — ყველა სიტყვა იწყება დიდი ასოთი
# ხშირად გამოიყენება კლასების სახელებისთვის
# მაგალითად: MyName, UserAge, TotalSum


# 2) კომენტარებით ახსენი, რა არის input და რა არის output. თითოეულზე მოიყვანე 2-2 მაგალითი.
# input — გამოიყენება მომხმარებლისგან მონაცემის მისაღებად
# მაგალითი 1:
name = input("შეიყვანეთ თქვენი სახელი: ")

# მაგალითი 2:
age = input("შეიყვანეთ თქვენი ასაკი: ")


# output — გამოიყენება შედეგის ეკრანზე გამოსატანად
# მაგალითი 1:
print("გამარჯობა")

# მაგალითი 2:
print(age)


# 3) მომხმარებელს შეაყვანინე შენი ასაკი და შექმენი ცვლადი snake_case-ით. შედეგად გამოიტანე თქვენს ასაკზე 1-დან 10-მდე ნებისმიერი რიცხვით გამოკლებული მნიშვნელობა.
user_age = int(input("შეიყვანეთ თქვენი ასაკი: "))  # snake_case ცვლადი

result = user_age - 7  # ასაკიდან 1-დან 10-მდე ნებისმიერი რიცხვის (მაგ: 7) გამოკლება
print(result)


# 4) შექმენი ცვლადი სახელად my_name და მომხმარებელს შეაყვანინე შენი სახელი.
# შექმენი მეორე ცვლადი სახელად user_name და მომხმარებელს შეაყვანინე შენი გვარი.
# საბოლოოდ, მოახდინე ამ ორ ცვლადზე კონკატინაცია.
my_name = input("შეიყვანეთ თქვენი სახელი: ")
user_name = input("შეიყვანეთ თქვენი გვარი: ")

full_name = my_name + " " + user_name  # კონკატინაცია
print(full_name)
