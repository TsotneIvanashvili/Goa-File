# ოჯახის წევრების სახელების სია
names = ["მამა", "დედა", "ძმა", "მე"]

# დაბეჭდეთ 1-ლი და 3-ე ელემენტები
print("პირველი ელემენტი:", names[0])   # ინდექსი 0 → პირველი
print("მესამე ელემენტი:", names[2])   # ინდექსი 2 → მესამე



import random

# 10 შემთხვევითი რიცხვის სია
numbers = [random.randint(1, 100) for _ in range(10)]

print("საწყისი სია:", numbers)

# 0-ლე ინდექსზე მდგომი ელემენტი შევცვალოთ ჩემს სახელზე
numbers[0] = "Tsotne"

print("შეცვლილი სია:", numbers)



# სია, სადაც სხვადასხვა ტიპის მნიშვნელობაა
mixed_list = [10, "hello", 3.14, True, [1,2,3], {"key":"value"}, None, "Python", 99, False]

# დაბეჭდეთ კენტ ინდექსზე მდგომი ელემენტები
print("კენტ ინდექსზე მდგომი ელემენტები:")
for i in range(len(mixed_list)):
    if i % 2 == 1:   # კენტი ინდექსი
        print(f"ინდექსი {i}: {mixed_list[i]}")