# ფუნქციის მთავარი უპირატესობაა კოდის მრავალჯერადი გამოყენება.
# მაგალითად, თუ გვჭირდება ერთი და იგივე მოქმედება ბევრჯერ, ფუნქცია გვიხსნის კოდის გამეორებისგან.
# ფუნქცია შეიძლება იღებდეს არგუმენტებს (შესატან მონაცემებს) და აბრუნებდეს შედეგს.



print("Hello")      # ბეჭდავს ტექსტს ეკრანზე
input("Enter: ")    # მომხმარებლისგან მონაცემის მიღება
len("Python")       # აბრუნებს სიგრძეს (აქ: 6)
type(123)           # აბრუნებს ობიექტის ტიპს (int)
str(123)            # რიცხვს გადააქცევს სტრინგად
int("45")           # სტრინგს გადააქცევს მთელ რიცხვად
float("3.14")       # სტრინგს გადააქცევს ათწილად რიცხვად
bool(0)             # აბრუნებს True/False მნიშვნელობას
list("abc")         # ქმნის სიას ['a','b','c']
tuple([1,2,3])      # ქმნის tuple-ს (1,2,3)
set([1,2,2,3])      # ქმნის set-ს {1,2,3}
dict(a=1,b=2)       # ქმნის ლექსიკონს {'a':1,'b':2}
range(5)            # ქმნის რიცხვების მიმდევრობას 0-დან 4-მდე
sum([1,2,3])        # აბრუნებს ჯამს (6)
max([1,5,3])        # აბრუნებს მაქსიმუმს (5)
min([1,5,3])        # აბრუნებს მინიმუმს (1)
sorted([3,1,2])     # აბრუნებს დალაგებულ სიას [1,2,3]
abs(-7)             # აბრუნებს მოდულს (7)
round(3.14159,2)    # აბრუნებს დამრგვალებულ მნიშვნელობას (3.14)
pow(2,3)            # აბრუნებს 2^3 = 8
enumerate(["a","b"])# აბრუნებს ინდექს+მნიშვნელობის წყვილებს
zip([1,2],[3,4])    # აერთიანებს ორ სიას წყვილებად
any([False,True])   # აბრუნებს True თუ хоть ერთი ელემენტი True-ა
all([True,True])    # აბრუნებს True თუ ყველა ელემენტი True-ა